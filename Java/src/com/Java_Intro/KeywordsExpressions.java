package com.Java_Intro;

public class KeywordsExpressions {
    public static void main(String[] args){
        // There are totally 52 registered keywords in Java
        double value = 10;
        System.out.println(value);
        // We cannot use the keyword or reserved words to declare the name of the variable.
        float area = 100*35;
        System.out.println(area);
        // float is keyword and area = 100*35 is expression
        if (area == 2500){
            System.out.println("This area is large");
        }
        //expressions are area == 2500


    }
}
